#include "libft.h"

int	strncmp(const char *s1, const char *s2, size_t n)
{
	while( *s1 == *s2 )
	{
		if ( *s1 == '\0' || !--n )
		{
			return 0;
		}
		*s1++;
		*s2++;
	}
	return (*s1 - *s2);
}
